/**
 * Defines a school.
 */
class School {
    /**
     * The ID of this school.
     */
    id: number;
    /**
     * The name of this school.
     */
    schoolName: string;
    /**
     * The Url for the logo of this
     * school.
     */
    schoolLogoUrl: string;
    /**
     * The address of this school.
     */
    schoolAddress: string;
}
export = School;